print(f'El resultado de la suma es {suma}')

resta = operando_a - operando_b
print (f' El resultado de la resta es: {resta}')

mutiplicacion = operando_a * operando_b

print(f'Resultado de la multiplicación: {mutiplicacion}')

division_float = operando_a / operando_b
#Acá por ejempleo Python nos regresa el resultado de la division
#en punto flotante.
print(f'El resultado de la division (float): {division_float}')

division_int = operando_a // operando_b

print(f'El resultatao de la division (int): {division_int}')

modulo = operando_a % operando_b

print(f'El resultatao del modulo de la división es: {modulo}')

exponente = operando_a ** operando_b

